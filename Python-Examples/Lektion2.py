
# While-loop
counter = 0
while counter < 5:
    print(f"While loop, iteration {counter}")
    counter += 1

# For-loop över en lista
fruits = ["äpple", "banan", "körsbär"]
for fruit in fruits:
    print(f"For loop, frukt: {fruit}")

# For-loop med range
for i in range(5):
    print(f"For loop med range, iteration {i}")
